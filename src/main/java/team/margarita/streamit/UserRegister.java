    /**
     * This method sends the information of a new user to the method wich will insert the new user
     * in the database
     * 
     * @author De Brabander Stef
     * @param surname
     * @param name
     * @param email
     * @param pwd 
     */
    private void checkRegister(String surname, String name, String email, String pwd) {
        boolean register = UserRequest.registerUser(surname, name, email, pwd);
        if(register) {
            new UserLogin().setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
    }
