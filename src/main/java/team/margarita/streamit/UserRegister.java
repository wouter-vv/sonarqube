    /**
     * executed when the register button is clicked
     * checks and sends the data
     * 
     * @param evt 
     */
    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        lblEmpty.setVisible(false);
        lblNotSame.setVisible(false);
        lblInvalidEmail.setVisible(false);
        
        String surname = txtSurname.getText();
        String name = txtName.getText();
        String email = txtEmail.getText();
        String pwd = txtPassword.getText();
        String pwd2 = txtVerifyPassword.getText();
        Pattern pattern = Pattern.compile("^.+@.+\\..+$");
        Matcher matcher = pattern.matcher(email);
        boolean bm = matcher.matches();
        boolean nonEmpty = false;
        
        if (surname.equals("") || name.equals("") || email.equals("") || pwd.equals("") || pwd2.equals("")) {
            lblEmpty.setVisible(true);
            nonEmpty = true;
        }
        if(!bm) {
            lblInvalidEmail.setVisible(true);
        }
        if (!pwd.equals(pwd2)) {
            lblNotSame.setVisible(true);
        }
        
        if(nonEmpty && bm && pwd.equals(pwd2)) {
            checkRegister(surname, name, email, pwd);
        }      
    }//GEN-LAST:event_btnRegisterActionPerformed
    
    /**
     * check if the email is formatted correctly
     * @param email
     * @return 
     */
    public boolean isValidEmailAddress(String email) {
           String ePattern = "^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\])|(([a-zA-Z\\-0-9]+\\.)+[a-zA-Z]{2,}))$";
           java.util.regex.Pattern p = java.util.regex.Pattern.compile(ePattern);
           java.util.regex.Matcher m = p.matcher(email);
           return m.matches();
    }

